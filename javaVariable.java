public class javaVariable {
    public static void main(String[] args)
    {

        //Java Variables
        int integerNum = 10;            //Integer Type
        float floatNum = 12.5f;         //Float Type
        char characterLetter = 'B';     //Character Type
        boolean boolExp = true;         //Boolean Type
        String stringName = "Bishal";   //String Type

        System.out.println(integerNum);
        System.out.println(floatNum);
        System.out.println(characterLetter);
        System.out.println(boolExp);
        System.out.println(stringName);

        //Print Variables + Concatenation

        String name = "Bishal Shrestha";
        System.out.println("Hello Mr."+ name);

        //Declare Multiple Variables
        int x,y,z;
        x=10;
        y=20;
        z=30;
        System.out.println(x+y+z);
        
        /*
         * Identifier should be identified with unique names
         * It is recommended to use descriptive names in order to create understandable and maintainable code:
         */

    }
}
